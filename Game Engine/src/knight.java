package utilities;

public class Knight{

	private int KnightHp=1000;
	private int Smack=18;
	private int Stab=36;

	public int getKnightHp() {
		return KnightHp;
	}
	public void setKnightHp(int knightHp) {
		KnightHp = knightHp;
	}
	public int getSmack() {
		return Smack;
	}
	public void setSmack(int Smack) {
		Smack = Smack;
	}
	public int getStab() {
		return Stab;
	}
	public void setStab(int Stab) {
		Stab = Stab;
	}
}
